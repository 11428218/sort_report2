#include <stdio.h>
#include <time.h>

#define N 10000

/* -------------------------------------------------------
 * bubble_sort: 氣泡排序函式
 * a[]: 待排序整數陣列 (傳址)
 * n: 陣列元素個數
 *
 * 演算法:
 * 1) 外迴圈 k 從 n-1 遞減到 1
 * 2) 內迴圈 i 從 0 到 k-1，做相鄰比較
 * 3) 若 a[i] > a[i+1]，以暫存變數做三步驟交換
 *
 * 複雜度:
 * 時間 O(n^2)
 * 空間 O(1)
 * ------------------------------------------------------- */
void bubble_sort(int a[], int n)
{
    for (int k = n - 1; k > 0; k--) {
        for (int i = 0; i < k; i++) {
            if (a[i] > a[i + 1]) {
                /* 三步驟交換，不能直接連續覆蓋 */
                int t = a[i];
                a[i] = a[i + 1];
                a[i + 1] = t;
            }
        }
    }
}

/* -------------------------------------------------------
 * main: 複雜度實驗
 * 1) 建立最差情況 (完全逆序) 陣列
 * 2) 用 clock() 量測 CPU 時間
 * 3) 輸出結果，填入實驗表格
 * ------------------------------------------------------- */
int main(void)
{
    int a[N];
    clock_t start, end;
    double cpu_time_used;

    /* 最差情況: N, N-1, ..., 1 */
    for (int i = 0; i < N; i++) {
        a[i] = N - i;
    }

    printf("排序前 (前 10 個): ");
    for (int i = 0; i < 10 && i < N; i++) {
        printf("%d ", a[i]);
    }
    printf("...\n");

    start = clock();
    bubble_sort(a, N);
    end = clock();

    cpu_time_used = (double)(end - start) / CLOCKS_PER_SEC;

    printf("排序後 (後 10 個): ... ");
    for (int i = N - 10; i < N; i++) {
        printf("%d ", a[i]);
    }
    printf("\n");

    printf("排序 %d 個元素，花費時間 = %.6f 秒\n", N, cpu_time_used);
    return 0;
}

